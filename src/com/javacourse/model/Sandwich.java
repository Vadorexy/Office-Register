package com.javacourse.model;

public class Sandwich {
        String main_ingredent;
        String bread_type;
        Double for_price;

    public String getMain_ingredent() {
        return main_ingredent;
    }

    public void setMain_ingredent(String main_ingredent) {
        this.main_ingredent = main_ingredent;
    }

    public String getBread_type() {
        return bread_type;
    }

    public void setBread_type(String bread_type) {
        this.bread_type = bread_type;
    }

    public Double getFor_price() {
        return for_price;
    }

    public void setFor_price(Double for_price) {
        this.for_price = for_price;
    }
}
